    <h1>Model View Controller</h1>
  </div>
  <p><strong>MVC</strong> is a software architecture, which separates the logic from the user interface. This is achieved by separating the application into three parts Model, View and Controller. MVC is separation of concern. MVC is also a design pattern.</p>
  <p><strong>Model</strong>: Represents the logical behavior of data in the application. It represents applications business logic. Model notifies view and controller whenever there is change in state.</p>
  <p><strong>View</strong>: Provides the user interface of the application. A view is the one which transforms the state of the model into readable HTML.&nbsp;</p>
  <p><strong>Controller</strong>: Accepts inputs from the user and instructs the view and model to perform the action accordingly.</p>  
